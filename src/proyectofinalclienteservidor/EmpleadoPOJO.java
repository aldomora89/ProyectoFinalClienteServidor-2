package proyectofinalclienteservidor;

/**
 *
 * @author Santiago
 */
public class EmpleadoPOJO {
    private String name;
    private int edad;
    private String id;
    private double salario;

    public EmpleadoPOJO(String name, int edad, String id, double salario) {
        this.name = name;
        this.edad = edad;
        this.id = id;
        this.salario = salario;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public double getSalario() {
        return salario;
    }

    public void setSalario(double salario) {
        this.salario = salario;
    }
    
    
    
}
